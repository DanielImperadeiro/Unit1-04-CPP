// This program displays the area and perimeter of a rectangle

#include <iostream>

int main() {
  std::cout << "For a rectangle that has the dimensions\n";
  std::cout << "length 5 cm and width 7 cm:\n";
  std::cout << "\narea = " << (5 * 7) << " cm^2";
  std::cout << "\nperimeter = " << (2 * (5 + 7)) << " cm";
}
